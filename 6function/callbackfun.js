/**
 * A callback function is a function passed into another function as an argument,
 which is then invoked inside the outer function to complete some kind of routine or action.

Callbacks are often used to continue code execution after an asynchronous operation has 
completed — these are called asynchronous callbacks. A good example is the callback 
functions executed inside a .then().

A callback’s primary purpose is to execute code in response to an event. 
These events might be user-initiated, such as mouse clicks or typing.
 */